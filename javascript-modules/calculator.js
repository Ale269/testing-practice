const calculator = {
  sum(a, b) {
    const result = a + b;
    return result;
  },
  subtraction(a, b) {
    const result = a - b;
    return result;
  },
  moltiplication(a, b) {
    const result = a * b;
    return result;
  },
  division(a, b) {
    const result = a / b;
    return result;
  },
};

module.exports = calculator;
